# list[start:end:step]

my_list = [*range(10)]

print(my_list[2:])
print(my_list[:5])
print(my_list[2:-1])
print(my_list[2:-1:2])
print(my_list[-1:2:-1])
print(my_list[-2:1:-2])
print(my_list[::-1])

my_url = "http://coreyms.com"

print(my_url)
print(my_url[::-1])
print(my_url[-4:])
print(my_url[7:])
print(my_url[7:-4])
